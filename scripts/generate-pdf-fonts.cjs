// scripts/generate-pdf-fonts.cjs
// Run: node scripts/generate-pdf-fonts.cjs
// Reads TTF files and generates src/lib/fonts/bvn-fonts.ts with embedded Uint8Array.

const fs = require('fs');
const path = require('path');

const FONTS_DIR = path.join(__dirname, '..', 'src', 'lib', 'fonts');
const REG = path.join(FONTS_DIR, 'BeVietnamPro-Regular.ttf');
const BOLD = path.join(FONTS_DIR, 'BeVietnamPro-Bold.ttf');
const OUT = path.join(FONTS_DIR, 'bvn-fonts.ts');

if (!fs.existsSync(REG) || !fs.existsSync(BOLD)) {
  console.error('Missing TTF files. Run:');
  console.error('  curl -L -o src/lib/fonts/BeVietnamPro-Regular.ttf "https://fonts.gstatic.com/s/bevietnampro/v12/QdVPSTAyLFyeg_IDWvOJmVES_Eww.ttf"');
  console.error('  curl -L -o src/lib/fonts/BeVietnamPro-Bold.ttf "https://fonts.gstatic.com/s/bevietnampro/v12/QdVMSTAyLFyeg_IDWvOJmVES_HSMIF8y.ttf"');
  process.exit(1);
}

const reg = fs.readFileSync(REG);
const bold = fs.readFileSync(BOLD);

function toUint8Literal(buf) {
  // Split into chunks for readability
  const lines = [];
  for (let i = 0; i < buf.length; i += 16) {
    const slice = buf.slice(i, i + 16);
    lines.push('  ' + Array.from(slice).map(b => '0x' + b.toString(16).padStart(2, '0')).join(','));
  }
  return 'new Uint8Array([\n' + lines.join(',\n') + '\n])';
}

const out = `// Auto-generated from BeVietnamPro TTF files
// Source: Google Fonts (https://fonts.google.com/specimen/Be+Vietnam+Pro)
// License: SIL Open Font License 1.1
// Generated by: scripts/generate-pdf-fonts.cjs — DO NOT EDIT MANUALLY

export const BE_VIETNAM_PRO_REGULAR: Uint8Array = ${toUint8Literal(reg)};

export const BE_VIETNAM_PRO_BOLD: Uint8Array = ${toUint8Literal(bold)};
`;

fs.writeFileSync(OUT, out);
console.log(`✓ Generated ${OUT} (${out.length} bytes, regular=${reg.length}B, bold=${bold.length}B)`);
